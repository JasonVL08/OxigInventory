package com.inventory.entity;

public class Cliente {
	
	private int id_cliente, telef_cliente;
	private String nombre_cliente, apellido_cliente, direc_cliente, tipodoc_cliente, numdoc_cliente;
	public int getId_cliente() {
		return id_cliente;
	}
	public void setId_cliente(int id_cliente) {
		this.id_cliente = id_cliente;
	}
	public int getTelef_cliente() {
		return telef_cliente;
	}
	public void setTelef_cliente(int telef_cliente) {
		this.telef_cliente = telef_cliente;
	}
	public String getNombre_cliente() {
		return nombre_cliente;
	}
	public void setNombre_cliente(String nombre_cliente) {
		this.nombre_cliente = nombre_cliente;
	}
	public String getApellido_cliente() {
		return apellido_cliente;
	}
	public void setApellido_cliente(String apellido_cliente) {
		this.apellido_cliente = apellido_cliente;
	}
	public String getDirec_cliente() {
		return direc_cliente;
	}
	public void setDirec_cliente(String direc_cliente) {
		this.direc_cliente = direc_cliente;
	}
	public String getTipodoc_cliente() {
		return tipodoc_cliente;
	}
	public void setTipodoc_cliente(String tipodoc_cliente) {
		this.tipodoc_cliente = tipodoc_cliente;
	}
	public String getNumdoc_cliente() {
		return numdoc_cliente;
	}
	public void setNumdoc_cliente(String numdoc_cliente) {
		this.numdoc_cliente = numdoc_cliente;
	}
	
	

}
